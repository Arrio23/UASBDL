    private void jButtonHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "DELETE FROM pelanggan WHERE id_pelanggan = '" + jTextFieldID.getText() + "'";
            java.sql.Connection conn = (Connection) koneksi.configDB();
            java.sql.PreparedStatement pstm = conn.prepareStatement(sql);
            pstm.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        masukTabel();
        clear();
    }//GEN-LAST:event_jButtonHapusActionPerformed

    private void bt_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_tambahActionPerformed
        // TODO add your handling code here:
        try {
            String sql = "INSERT INTO pelanggan VALUES (?, ?, ?, ?, ?)";
            Connection konn = (Connection) koneksi.configDB();
            PreparedStatement stat = konn.prepareStatement(sql);
            stat.setString(1, jTextFieldID.getText());
            stat.setString(2, jTextFieldNama.getText());
            stat.setString(3, jTextFieldNomor.getText());
            stat.setString(4, jTextFieldEmail.getText());
            stat.setString(5, jTextFieldAlamat.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan!");
            clear();
            jTextFieldID.requestFocus();
            masukTabel();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_bt_tambahActionPerformed

